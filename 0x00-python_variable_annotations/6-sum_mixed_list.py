#!/usr/bin/env python3
"""6-sum_mixed_list.py"""

from typing import Union, List


def sum_mixed_list(mxd_lst: List[Union[int, float]]) -> float:
    """
    sum of a list

    Args:
        mxd_lst(list): lists of numbers

    Returns:
        float: sum of the list
    """
    return sum(mxd_lst)
